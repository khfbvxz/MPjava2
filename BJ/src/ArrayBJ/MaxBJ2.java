package ArrayBJ;

import java.util.Scanner;

public class MaxBJ2 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int  max=-111111 , index=0;
		int [] arr = new int[9];
		for(int i = 0 ; i<9 ; i++) {
			arr[i] = Integer.parseInt(sc.nextLine());
		}
		for(int i = 0 ; i<9 ; i++) {
			if(arr[i] > max) {
				max = arr[i];
				index = i;
			}
		}
		System.out.println(max);
		System.out.println(index);
	}
}
